<h1 class ="col-xs-12 col-sm-12 col-md-12 col-lg-12">Educación</h1>
<article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
	<p>El Consejo Supremo de Educación y el Ministerio de Educación dirigen el sistema educativo en Qatar. El Consejo Supremo de Educación está llevando lentamente a lo largo de las responsabilidades del Ministerio de Educación, como el sistema educativo del país se encuentra actualmente en un período de transición.
	<br>
	Las escuelas públicas son libres de todos los ciudadanos en Qatar, y la escuela primaria es obligatoria para todos niños. Las escuelas se dividen normalmente por género, excepto en las instituciones privadas e internacionales, en los que pueden ser co-educacional o dividido por género en los años superiores. El año escolar en Qatar es desde principios de septiembre hasta el tercera semana de junio y se divide en dos términos.
	<br>
	Hay cinco etapas de la educación en Qatar (aunque estos pueden variar y se hace referencia a la utilización de vocabulario diferente en las escuelas privadas e internacionales veces):
	</p>
	<ol>
		<li>Etapa preescolar (3 a 5 años) - preescolar está dirigido principalmente por el sector privado en Qatar.</li>
		<li>Etapa primaria (grados 1 a 6) - la edad de admisión es normalmente 6. La educación primaria es obligatoria.</li>
		<li>Etapa preparatoria (grados 7 a 9) - Al término de la escuela preparatoria, los niños pueden proceder a una escuela secundaria general, técnica o comercial.</li>
		<li>Etapa secundaria (Grados 10 a 12)</li>
		<li>Postsecundaria Escenario</li>
	</ol>


		<p>Qatar actualmente cuenta con 113 escuelas primarias: 60 para los varones y 53 para las niñas. También cuenta con 56 escuelas preparatorias: 28 para los varones y 28 para las niñas. Por último, tiene 41 escuelas secundarias: 19 para los varones y 22 para las niñas.
		</p>
		<h2>Escuelas privadas en Doha</h2>

		<p>El gobierno de Qatar apoya a las instituciones educativas privadas, proporcionándoles libros de texto gratuitos, salud, electricidad y agua. También proporciona una directora de Qatar y maestro de cada jardín de infancia. Esto ha conducido a un enorme ascenso de la popularidad de las escuelas privadas en Qatar.
		</p>
		<br>
		<p>La entrada a la mayoría de las escuelas privadas en Doha se limita a los nacionales de Qatar. Hijos de expatriados de pueden asistir internacional escuelas.</p>
		

		<h2>Universidades de Qatar</h2>
		<p>Para los ciudadanos de Qatar y ciudadanos de otros países del CCG, becas gubernamentales y / o patrocinios de empresas suelen cubrir la matrícula universitaria. La duración del curso depende de la escuela y el programa y varían de 1 a 4 años.</p>
