  <h1 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">HISTORIA</h1>

  <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Primeros pobladores</h2>
  <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">

	<p>Hacia el año 10 000 a. C. los íberos comenzaron a poblar el interior de las tierras de la península a la que darían nombre. Entre el 4000 a. C. y el 2000 a. C., Portugal y Galicia vieron como se desarrollaba una cultura megalítica original, caracterizada por su arquitectura funeraria, sus rituales propios y por la práctica de la inhumación colectiva<br>
	En la Edad del Bronce hubo unos primeros contactos marítimos entre el litoral atlántico y el de las islas británicas, mientras que el sur de la península empezaba sus relaciones comerciales con el Mediterráneo: griegos y fenicios, provenientes del actual Líbano,  Esto trajo consigo la instalación de los primeros puestos comerciales semipermanentes.<br>

	Durante la Edad del Hierro, un pueblo indoeuropeo se estableció por toda la región: los celtas. Estos ocuparon todo el territorio hoy conocido como Portugal, vivieron en pequeños núcleos de población aislados que se encontraban en los puntos altos con casas circulares o castros y practicaron la agricultura y la ganadería.21 Con su dominio del hierro los trabajos de la tierra fueron más eficaces, las cosechas aumentaron y mejoraron las condiciones de vida y la demografía.
	</p>
