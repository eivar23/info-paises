    <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">La Era de los Descubrimientos </h2>
    <article  class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
		<p>Con el final de la guerra, Portugal inició un proceso de exploración y expansión conocido como «Era de los Descubrimientos», cuyas figuras destacadas fueron el infante Enrique el Navegante y el rey Juan II. Tras la conquista de Ceuta en 1415 y el paso del cabo Bojador por Gil Eanes, la exploración de la costa africana continuó hasta que Bartolomé Díaz comprobó en 1488 la comunicación entre los océanos Índico y Atlántico al doblar el cabo de Buena Esperanza. 
		En poco tiempo los portugueses descubrieron rutas y tierras en Norteamérica, Sudamérica y Oriente, en su mayoría durante el reinado de Manuel I, el Aventurero. La expansión hacia Oriente, sobre todo gracias a las conquistas de Alfonso de Alburquerque, concentró casi todos los esfuerzos de los portugueses, aunque en 1530 Juan III inició la colonización de Brasil. Las riquezas allí encontradas hicieron que los portugueses se centraran en el Nuevo Mundo, con la consiguiente pérdida de otras plazas en el Índico, como Ormuz, frente a otras potencias europeas.
		</p>
