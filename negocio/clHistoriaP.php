  <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Formación y consolidación del reino</h2>
  <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
    <p>
  	Con la llegada de Alfonso Enríquez, Portugal consiguió la independencia con la firma en 1143 del tratado de Zamora y reconocida por el papa Alejandro III en la bula Manifestis Probatum en 1179. Posteriormente, conquistó localidades importantes como Santarém, Lisboa, Palmela y Évora. 
  	Una vez acabada la Reconquista portuguesa en 1249, la independencia del nuevo reino fue puesta en entredicho varias veces por el reino de Castilla. En una de estas situaciones de conflicto con el reino de Castilla, el rey Dionisio I de Portugal firmó junto al rey Fernando IV de Castilla el Tratado de Alcañices, en el cual se estipulaba que Portugal suprimía los tratados acordados en contra del reino de Castilla por el apoyo al infante Juan de Castilla. En este tratado se establecía entre otras cosas la delimitación fronteriza entre los entonces reinos de Portugal y de León, en la que se incluía la cuestionada localidad de Olivenza. La primera fue debida a la crisis sucesoria abierta tras la muerte de Fernando I de Portugal, que acabó con la victoria portuguesa en Aljubarrota en 1385.
    </p>
