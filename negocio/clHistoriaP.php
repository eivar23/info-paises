    <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">República, Estado Nuevo y democracia</h2>
    <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
		<p>
		El rey salió hacia el exilio en Inglaterra tras la instauración de la república. Después de varios años de inestabilidad política, con luchas de trabajadores, tumultos, levantamientos, homicidios políticos y crisis financieras, problemas agravados por la participación portuguesa en la Primera Guerra Mundial, el ejército tomó el poder en 1926. Dos años más tarde, el régimen militar nombró ministro de Finanzas a António de Oliveira Salazar, profesor de la universidad de Coímbra, que en 1932 se convirtió en presidente del consejo de ministros.<br>
		Salazar restauró las finanzas e instituyó el Estado Nuevo, régimen autoritario de corporativismo de Estado con un partido único y sindicatos estatales, además de una afinidad fascista bien marcada, al menos hasta 1945, cuando tras la victoria de los Aliados en la Segunda Guerra Mundial, Salazar sufrió presiones para transformar Portugal en una democracia. En 1968, apartado del poder por una enfermedad, fue sucedido por Marcelo Caetano.<br>
		Entre las décadas de 1940 y 1960, Portugal fue miembro cofundador de la OTAN (1949), la OCDE (1961) y la EFTA (1960), de la que se salió en 1986 para adherirse a la entonces CEE. En 1999, Portugal se adhirió a la zona Euro y ese mismo año entregó la soberanía de Macao a la República Popular China. Desde su adhesión a la UE, el país ha presidido el Consejo Europeo tres veces, la última en 2007 cuando presidió la ceremonia de la firma del tratado de Lisboa. </p>
