    <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Restauración, absolutismo y liberalismo</h2>
    <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
	
		<p>
		El final del siglo XVII y la primera mitad del siglo XVIII fueron testigos del florecimiento de la minería en Brasil: 
		el descubrimiento de oro y piedras preciosas convirtió a la corte de Juan V en una de las más opulentas de Europa. Estas riquezas sirvieron para pagar productos importados, en su mayoría de Inglaterra, ya que no existía industria textil en el reino y las telas eran importadas de las Islas Británicas. 
		El comercio exterior se basaba en la industria del vino y los esfuerzos para invertir la situación con grandes reformas mercantiles del marqués de Pombal, ministro entre 1750 y 1777, impulsaron el desarrollo económico durante el reinado de José I. Fue durante este reinado cuando un terremoto devastó Lisboa y el Algarve, el 1 de noviembre de 1755.
		</p>
