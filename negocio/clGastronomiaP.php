    <h1 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Gastronomía</h1>

    <article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
    <p>La gastronomía es variada. Cada zona del país tiene su plato típico compuesto por diversas carnes (ternera, oveja, cerdo y las carnes blancas), por diferentes embutidos, tipos de pescados (sobre todo de bacalao) y de mariscos. Entre los quesos destacan el Serra da Estrela, el Azeitão y el São Jorge, entre muchos otros.
    Portugal es un país fuertemente vinícola, y son célebres los vinos del Douro, del Alentejo y del Dão, los vinos verdes del Minho, y los vinos licorosos de Oporto y de Madeira. En cuanto a la repostería existen una enorme variedad de recetas tradicionales como los famosos pasteles de Belém, los huevos moles de Aveiro, el pastel de Tentúgal, la sericaia o el bizcocho de Ovar, así como muchos otros.
    De entre los platos populares, destacan el cocido a la portuguesa, el «bacalao dorado», el «bacalao a la Gomes de Sá», las espetadas de Madeira, el «cocido volcánico» de las Azores, el «lechón asado a la Bairrada», los «Rojões» del Aveiro y del Minho, la chanfana de Beira, la carne de cerdo a la alentejana, el pescado asado (en todo el país), las tripas (de la región de Oporto), las «pataniscas de bacalao» y el gazpacho portugués. La cocina portuguesa ha influido otras gastronomías como la japonesa, con la introducción de la tempura.
    </p>
    </article>

    <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Ingredientes</h2>

    <article class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
    <h3> Ajos.</h3>
    <p>Se emplea el ajo muy extensamente, algunas especias en forma de hierba como el coriandro o  cilantro y el perejil, el azafrán o el mismo jengibre en recetas tradicionales. En los restaurantes y en ocasiones especiales se sirven aceitunas y queso como aperitivos. Es muy frecuente servir una sopa antes del plato principal, acompañar los platos de carne con arroz blanco y los de pescado con patatas cocidas. No es raro que arroz y patatas se presenten juntos en el mismo plato</p>
    </article>
    <img src="../imagenes/gastronomia/ajos.jpg" id="imgCont" class="col-xs-12 col-sm-12 col-md-4 col-lg-4 " width="50%" height="50%" style="padding-top: 3%">

    <article class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
    <h3>Pan</h3>

    <p>El pan es uno de los elementos básicos de la cocina portuguesa. El pan forma parte de platos muy tradicionales como las açordas y las migas à alentejana y el torricado. 
    Entre los panes más conocidos allende se encuentran las Broa de Avintes. Otros panes son la Fogaça, las “caralhotas” de Almeirim (se trata de panes redondos y de tamaño medio, especialmente apreciados cuando son recién salidos del horno), el "pão-com-chouriço" (similar a los preñaos asturianos se consumen en ferias y fiestas tienen un chouriço en su interior), los folares (propios de Pascua), etc. En el norte de Portugal, se refiere a menudo a las "bolas" ("bôlas") que son panes con carne picada en su interior (en Trás-os-Montes) o panes redondos y compactos servidos con sardinhas o carne (como se puede encontrar en algunas partes del Minho).</p>
    </article>
    <img src="../imagenes/gastronomia/broaavintes.jpg" id="imgCont" class="col-xs-12 col-sm-12 col-md-4 col-lg-4 " width="50%" height="50%" style="padding-top: 3%">

    <article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
    <h2>El aceite de oliva</h2>
    <p>El aceite de oliva (azeite)1 es uno de los pilares de la cocina portugesa, se emplea tanto para asar las carnes y los pescados, como en aliños de ensaladas y verduras diversas. Existen seis denominaciones de origen protegida (DOP), y son: Azeite de Moura, Azeite de Trás-os-Montes, Azeite do Alentejo Interior , Azeites da Beira Interior (Azeite da Beira Alta y Azeite da Beira Baixa), Azeites do Norte Alentejano y Azeites do Ribatejo. Se emplea mucho para hacer sofritos (Refogado) de ajo y cebolla.
    </p>
    </article>

    <article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
    <h2>Carnes</h2>
    <p>En el terreno de las carnes se come mucho porcino y se elaboran embutidos (Enchidos), tales como el Chouriço, el jamón (llamado presunto, frecuentemente curado al humo, de sabor muy característico). Como en la cocina de España es tradicional la matança do porco (matanza), que consiste en el sacrificio de un cerdo y su posterior procesado para la alimentación de una unidad familiar. La chanfana utiliza carne de cabra cocida en vino. En el terreno de las aves se tiene el arroz con pato y la canja a doentes, sopa con carne de gallina. El bistec a la portuguesa se hace untando la carne con pasta de ajos.
    <br>Las carnes de caza menor suelen ser el coelho (conejo), preparado a veces con Vinhadalhos. El coelho à caçadora (conejo a la cazadora).</p>
    </article>

    <article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
    <h2>Pescados</h2>
    <p>En el terreno de los pescados (Peixe en portugués), hay que destacar la tradición del consumo de pescado fresco, muchas veces cocinado de la manera más simple, a la brasa, y condimentado con aceite de oliva. También se prepara en una variedad de arroces caldosos o guisos de patatas llamados caldeiradas y cataplanas. Suelen ser muy conocidos los espetos de peixes (espetada) , que son una especie de brocheta. Es muy popular la sepia y los calamares (denominados lulas o lulinhas). Se consumen igualmente pulpo y diversos mariscos en el sur de Portugal: sobre todo langosta y bogavante, se suelen servir en las cervecerías a la parrilla o con huevo revuelto. En las zonas costeras se puede comer marisco en relativa abundancia.</p>
    </article>


