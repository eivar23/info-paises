    <h2>Bebidas</h2>

    <h3>Vinos</h3>
    <p>Los vinos son muy conocidos fuera de las fronteras de Portugal, se elaboran en casi todo el territorio y destacan los vinos verdes del norte que suelen ser vinos blancos y jóvenes, el Oporto (elaborado en la ciudad de Vila Nova de Gaia), Madeira, vino de Carcavelos, o el moscatel de Setúbal, tinto de Borba o Dão, entre otros. Catalogados como los mejores del planeta.</p>

    <h2>Platos</h2>
    <p>Es muy frecuente que una comida portuguesa completa consista en una sopa, un plato principal de carne o pescado y un postre dulce. Las sopas son variadas, las más frecuentes son el caldo verde (caldo realizado con col finamente picada y patata) la sopa de nabiças, el Cozido à portuguesa y en el terreno de las sopas frías, el gazpacho.

    </p>
