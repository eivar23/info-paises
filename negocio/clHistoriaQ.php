  <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
    <p>Obtenida su independencia (1971), se retiró de los Emiratos Árabes Unidos, firmó un tratado de amistad con Gran Bretaña e ingresó en la Liga Árabe y en la ONU (1971). Con un incruento golpe de Estado (1972), el emir Ahmad bin Ali fue depuesto por su primer ministro, que reunió ambos cargos. Catar condenó los acuerdos de Camp David entre Egipto, Israel y los Estados Unidos (septiembre de 1978), pero está estrechamente aliado con Arabia Saudita y es usualmente considerado como uno de los Estados árabes más moderados. En 1981 Catar se integró en el recién constituido Consejo de Cooperación del Golfo, y en 1983 sus fuerzas armadas participaron en los ejercicios militares conjuntos que los países miembros del Consejo desarrollaron en Omán.
    El 27 de junio de 1995 Khalifa bin Hamad Al Thani fue derrocado de forma incruenta por su hijo Hamad Al Thani, mientras pasaba sus vacaciones en Suiza. Tras convertirse en el nuevo emir, Hamad afrontó diversas reformas destinadas a la apertura y la democratización del sistema político. Así, los comicios municipales de 1999 fueron las primeras elecciones populares de la historia del país (en ellas, además, se permitió que las mujeres votaran). En abril de 2003 una nueva Constitución que reconocía la libertad de expresión, de asociación y de culto fue aprobada en referéndum.</p>

