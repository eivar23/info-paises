  <h1 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">HISTORIA</h1>
 


   <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">

  <p>Qatar ha sido habitado desde el paleolítico. Los primeros habitantes conocidos fueron los cananeos. Más tarde el país estuvo bajo el poder de varios gobernantes, como Sargón I (2335-2279 a. C.) El islam llegó a la península en el siglo VII d. C. y Catar pasó a formar parte, sucesivamente, del califato árabe y del Imperio otomano.</p>

  <p>Catar estuvo sometido al sultanato de la familia Al Khalifa de Baréin hasta 1868, en que, a petición de los nobles cataríes, fue instaurada la dinastía actual de los Al-Thani (āl-Tānī), con ayuda de los británicos. Terminó con la ocupación de Catar por los turcos otomanos en 1872, cuando la familia Al Khalifa se mudó a Baréin. En 1871 la dinastía reconoció la autoridad otomana, pero intentó conseguir la independencia en 1893 y 1898. Los turcos concedieron la autonomía del país en 1913. En el proceso de desmembramiento del Imperio otomano, a causa de la Primera Guerra Mundial. Un tratado de 1934 concedió protección británica más extensa. La explotación de los yacimientos petrolíferos (1949) no alteró sustancialmente las estructuras feudales del país. Tras la decisión británica de abandonar el Golfo Pérsico en 1971, Catar ingresó en la federación de Emiratos Árabes Unidos (1968).</p>
