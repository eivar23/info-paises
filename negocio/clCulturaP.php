<h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Fiestas Populares Y Peregrinaciones</h2>
<img src="../img/cultura/descarga.jpg" class="col-xs-12 col-sm-12 col-md-4 col-lg-4" width="30%" height="50%">

<article class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
<p>
Las fiestas y romerías son otros aspectos más presentes en las tradiciones portuguesas y vienen de la mano con algunas áreas mencionadas anteriormente, como la gastronomía y la música.

Partes como Santo Antonio, San Pedro y São João y las peregrinaciones al santuario de Nuestra Señora de Fátima, entre otros, se mueven a millones de turistas nacionales y extranjeros a través de las calles de las grandes ciudades y pueblos de Portugal, en parte o en la devoción, jugando un papel muy importante en las tradiciones portuguesas.

Las fiestas de San Antonio, en Lisboa, son los primeros festivales más grandes del país; se celebran durante la semana del 13 de junio, se adornan las casas con albahaca, se comen sardinas asadas y pan de maíz y, puesto que es considerado el santo del matrimonio, se intentan celebrar muchas bodas durante estas fiestas.

Como Santo Antonio de Lisboa, también en Oporto y Braga en el São João reúne a miles de personas y diversión, siendo el punto más alto de la noche del 23 a 24 junio; una fiesta extendida a otros puntos del mundo y más conocida como “La noche de San Juan”, en la que se encienden hogueras para ahuyentar los malos augurios.

También las peregrinaciones al santuario de la Virgen de Fátima mueven a muchas personas y son parte de las tradiciones portuguesas. Las principales fechas son, sin lugar a dudas, el 13 de mayo, fecha en que se celebra la primera aparición de la Virgen a tres pastores, en el pueblo de Fátima en 1917. La otra fecha importante es el 13 de octubre, que celebra la última aparición de la Virgen.
</p>
</article>

<h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Los azulejos</h2>

<article  class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
<p>
Los azulejos se han utilizado de muchas maneras en Portugal, en las fachadas de los edificios más grandes y más pequeños, a menudo con una sorprendente riqueza de color y complejidad. De hecho, todo el interior de muchas iglesias están cubiertas y bellamente pintadas con decoración de azulejos.
La palabra azulejo viene del árabe azuleich, y el arte parece haberse originado entre los asirios en la época anterior a Jesucristo. Los persas adoptaron la técnica antes de enseñarla a los árabes, que lo llevaron a Europa. Se fundó una fábrica en Sevilla, donde los primeros azulejos fueron realizados en motivos geométricos. Durante el siglo 17, Portugal aprobó su propio estilo de pintura de azulejos<br>
</p>
</article>
