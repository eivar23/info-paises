  <h1  class="col-xs-12 col-sm-12 col-md-12 col-lg-12">Musica</h1>
  <article class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
 
 <p>Las tradiciones portuguesas también están marcadas por la música. Uno de los géneros musicales característicos en Portugal, y único a nivel mundial, es el fado. 
Este género musical comenzó a destacarse en Portugal en la segunda mitad del siglo XIX, pero fue sólo durante las décadas de los 30 y 40 del próximo siglo que ganó proyección, por medio de la difusión tales como el cine, el teatro y la radio Hay varios aspectos que marcan este género musical como la melancolía, la añoranza y el destino; sobre la cantante de fado, que tiene una forma única de vestir y canta como ninguna otra cantante de otros géneros. Cabe señalar que el fado fue considerado por la UNESCO como patrimonio inmaterial. <br>
</p>
</article>
