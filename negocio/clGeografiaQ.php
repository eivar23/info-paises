  <p>La península catarí se adentra 150 km en el golfo Pérsico desde Arabia Saudita. Gran parte del país es una planicie baja y estéril, cubierta de arena. Al sureste está el Khor al Adaid o "mar interior", un área de dunas movedizas de arena que rodean una bahía del golfo. El punto más alto de Catar se encuentra en el Jebel Dukhan al oeste, una serie de bajos afloramientos de piedra caliza que corre de sur a norte desde Zikrit a través de Umm Bab hasta la frontera austral, y que alcanza alrededor de 90 metros sobre el nivel del mar. En esta zona también se hallan los principales depósitos interiores de petróleo de Catar, mientras que los yacimientos de gas natural están en el mar, al noroeste de la península. El territorio ocupa 11 586 km²,1 una superficie similar a la de la isla de Jamaica.
  <br>
  En la Edad del Bronce hubo unos primeros contactos marítimos entre el litoral atlántico y el de las islas británicas, mientras que el sur de la península empezaba sus relaciones comerciales con el Mediterráneo: griegos y fenicios, provenientes del actual Líbano,  Esto trajo consigo la instalación de los primeros puestos comerciales semipermanentes.

  Durante la Edad del Hierro, un pueblo indoeuropeo se estableció por toda la región: los celtas. Estos ocuparon todo el territorio hoy conocido como Portugal, vivieron en pequeños núcleos de población aislados que se encontraban en los puntos altos con casas circulares o castros y practicaron la agricultura y la ganadería.21 Con su dominio del hierro los trabajos de la tierra fueron más eficaces, las cosechas aumentaron y mejoraron las condiciones de vida y la demografía.
