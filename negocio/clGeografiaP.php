  <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">CLIMA</h2>
  <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
  
  <P>Portugal tiene un clima mediterráneo. Es uno de los países europeos con mayor diversidad climática: la temperatura media anual en el Portugal continental varía de los 13 °C en el interior montañoso hasta los 18 °C en el sur, en la depresión del Guadiana. Los veranos son agradables en las tierras altas del norte del país y en la región litoral del extremo norte y en el centro. El otoño y el invierno son típicamente ventosos, lluviosos y frescos y son más fríos en los distritos del norte y del centro del país, en los cuales las temperaturas son negativas en los meses más fríos. Sin embargo, en las ciudades más al sur, las temperaturas solo bajan de los 0 °C en contadas ocasiones y rondan los 5 °C en la mayoría de los casos. 
  Normalmente, los meses de primavera y verano son soleados y las temperaturas son altas durante los meses secos de julio y agosto en los que se pueden superar en algunas ocasiones los 40 °C en buena parte del país,  aunque con mayor frecuencia en el interior del Alentejo. 
  Nieva de forma regular en cuatro distritos del norte de Portugal (Guarda, Bragança, Vila Real y Viseu) pero su frecuencia disminuye hacia el sur, hasta ser inexistente en la mayor parte del Algarve. En invierno se registran temperaturas inferiores a -10 °C y se producen nevadas con cierta frecuencia en puntos aislados, como la sierra de la Estrella, la sierra de Gerês y la sierra de Montesinho, donde puede nevar de octubre a mayo. 
  </P>
