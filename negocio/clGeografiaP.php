  <h1 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">GEOGRAFÍA</h1>
  <article class="col-xs-12 col-sm-12 col-md-8 col-lg-8">
    
    <p>Portugal está situado en el suroeste de Europa, abarca 92 090 km² de superficie. El país ocupa una gran parte de la costa atlántica de la península ibérica y dos archipiélagos situados en el océano Atlántico: Madeira y Azores, con un total de 440 km² de zona marítima. Su zona continental tiene frontera con una única nación, España, al este y al norte, a lo largo de 1224 km,3 limitando al sur y al oeste con el océano Atlántico Norte a lo largo de 1793 km de costa.<br>
    Su territorio continental es atravesado por su río principal, el Tajo, que divide el país en dos por la mitad. En el norte, el paisaje es montañoso en las zonas del interior con planaltos intercalados que permiten el desarrollo de la agricultura. En el sur, hasta el Algarve, el relieve se caracteriza por la presencia de planicies, aunque existen algunas sierras intercaladas. Otros ríos principales son el Duero, el Miño y el Guadiana que, al igual que el Tajo, nacen en España. Entre los ríos que tienen todo su curso por territorio portugués destacan el Voga, el Sado y el más largo, el Mondego, que nace en la sierra de la Estrella. En esta sierra donde se halla la montaña más alta del Portugal continental y el segundo pico más alto de Portugal, con 1993 msnm de altitud.
    La costa portuguesa es extensa: tiene 1230 km en Portugal continental, 667 km en las Azores y otros 250 km en Madeira, donde también se incluyen las islas Salvajes, islas Desertas y la isla de Porto Santo. La costa está formada por playas con multitud de acantilados y arenales. Una peculiaridad importante de la costa portuguesa es la ría de Aveiro, estuario del río Voga, cerca de la ciudad de Aveiro, con 45 km de largo y un máximo de 11 km de ancho, que es rica en peces y aves marinas. 
    </p>
