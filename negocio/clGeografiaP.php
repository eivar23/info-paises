  <h2 class="col-xs-12 col-sm-12 col-md-12 col-lg-12">FLORA Y FAUNA</h2>
  <article class="col-xs-12 col-sm-12 col-md-6 col-lg-6">
    <p>La flora y fauna de Portugal se divide entre dos regiones biogeográficas bien diferenciadas: la región Macaronésica (Azores y Madeira) y la península ibérica (Portugal continental).</p>
    <p>Destaca en su patrimonio natural, un lugar declarado patrimonio de la Humanidad por la Unesco en 1999 por el tamaño y calidad de la laurisilva, un tipo de bosque de laurel: la Laurisilva de Madeira. Cuenta con siete reservas de la biosfera. 
    <br>
    En cuanto a los bosques de Portugal continental, están muy difundidos por razones económicas el pino, especialmente el resinero y el piñonero el castaño, el alcornoque la encina el quejigo y el eucalipto. </p>
    <h2>Fauna</h2>
    <p>La fauna de mamíferos es muy variada e incluye el zorro, el tejón, el lince ibérico, el lobo ibérico, la cabra montés, el gato montés, la liebre, la comadreja, el meloncillo, la jineta y, ocasionalmente, el oso pardo (cerca del río Miño y del Parque Nacional de Peneda-Gerês),117 entre otros. Portugal es un lugar de parada importante para las migraciones de avesque se desplazan entre Europa y África, especialmente en lugares como el cabo de San Vicente o la sierra de Monchique. El país tiene cerca de 600 especies de aves, de las cuales 235 son nidificantes, y casi todos los años hay registros nuevos.
    <br>
    Portugal tiene un gran número de especies de peces de agua dulce que van desde el pez gato gigante en el Parque Natural del Tajo Internacional, hasta las pequeñas especies endémicas que solo viven en pequeños lagos. Algunas de estas especies raras están gravemente amenazadas debido a la destrucción de su hábitat, la contaminación y las sequías. Las aguas marinas portuguesas son de las más ricas en biodiversidad del mundo, pues sus especies marinas rondan el millar e incluyen la sardina, el atún y la caballa del Atlántico.</p>
    </article>
