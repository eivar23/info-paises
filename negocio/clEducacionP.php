	<h1 class ="col-xs-12 col-sm-12 col-md-12 col-lg-12">Educación</h1>
	<article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
	<p>En Portugal, la educación escolar se desarrolla en tres niveles: la educación básica, la secundaria y la superior,la educación preescolar no es obligatoria y se dirige a niños de entre los tres años y la edad de ingreso en la enseñanza básica,la educación básica es universal, obligatoria y gratuita y comprende tres ciclos secuenciales: el primero de cuatro años, el segundo de dos y el tercero de tres, la educación secundaria es obligatoria y comprende un ciclo de tres años (10.º, 11.º y 12.º años de escolaridad).
	</article>

	<article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
	<h3>Educación básica</h3>
	<p>	Entre los 6 y los 14 años y se divide en tres ciclos: 4 años de enseñanza, correspondientes al 1.º ciclo de la educación básica; seguido de dos años correspondientes al 2.º ciclo de la educación básica y, para finalizar, 3 años, que corresponden al 3.º ciclo de la educación básica. Tras esta fase, los alumnos deberán ingresar en la educación secundaria.</p>
	


	<h3>Educación secundaria</h3>
	<p>Esta etapa tiene una duración de 3 años. Cuando se concluye, los alumnos pueden proseguir los estudios en la universidad o ingresar en la vida activa.</p>



	<h3>Educación superior</h3>
	<h4>Organización de la educación superior</h4>
	<p>En 2005 se dieron los primeros pasos para reformar el sistema de educación superior, con la introducción de un nuevo sistema de créditos (ECTS) para ciclos de estudio, mecanismos de movilidad o suplementos al diploma, entre otros. Se introdujeron cambios en la ley de bases del sistema educativo con el fin de implementar el Proceso de Bolonia.</p>
	<br>
	<p>La nueva estructura organizada en tres ciclos de estudio se introdujo en 2006 y se implementó plenamente en Portugal a partir del año lectivo 2009-2010. Los descriptores de cualificación genéricos se establecieron también para cada ciclo de estudios, basándose en las competencias adquiridas, así como la definición de intervalos ECTS para el primer y segundo ciclo de estudios.</p>
	<br>
	<p>La educación superior portuguesa comprende la educación universitaria y la educación politécnica. La educación universitaria se imparte en instituciones universitarias públicas y privadas y la educación politécnica en instituciones públicas y privadas de educación superior no universitarias. Los establecimientos de educación privada obtienen el reconocimiento previo del Ministerio de Educación y Ciencia. La red de educación superior integra también una institución de educación concordataria.</p>

	</article>

	<article class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
	<h3>Grado de licenciado</h3>
	<p>Las instituciones universitarias y politécnicas conceden el grado de licenciado.

	El ciclo de estudios que conduce al grado de licenciado en la educación politécnica tiene una duración normal de seis semestres curriculares de trabajo de los alumnos correspondientes a 180 créditos o, excepcionalmente, en casos cubiertos por normas jurídicas nacionales o de la Unión Europea, una duración normal de hasta siete u ocho semestres curriculares de trabajo y una formación de hasta 240 créditos.

	El ciclo de estudios que conduce al grado de licenciado en la educación universitaria tiene 180 o 240 créditos y una duración normal que comprende entre seis y ocho semestres curriculares de trabajo de los alumnos. En el primer ciclo de estudios de las instituciones universitarias o politécnicas el grado de licenciado se concede a los que, tras aprobar todas las unidades curriculares que integran el plan de estudios del curso de licenciatura, hayan obtenido el número de créditos fijado.

	 Título de máster

	Las instituciones universitarias y politécnicas conceden el título de máster.

	El ciclo de estudios que conduce al título de máster tiene de 90 a 120 créditos y una duración normal de entre tres y cuatro semestres curriculares de trabajo de los alumnos o, excepcionalmente a consecuencia de una práctica estable y consolidada internacionalmente, 60 créditos y una duración de dos semestres.

	En la educación politécnica el ciclo de estudios que lleva al título de máster debe asegurar, predominantemente, la adquisición de una especialización de naturaleza profesional. En la educación universitaria el ciclo de estudios para llegar al título de máster debe asegurar, predominantemente, la adquisición de una especialización de naturaleza académica con recurso a la actividad de investigación o que profundice competencias profesionales.

	En la educación universitaria el título de máster puede igualmente ser concedido tras un ciclo de estudios integrado, de 300 a 360 créditos y una duración normal comprendida entre 10 y 12 semestres curriculares de trabajo en los casos en que la duración para acceder al ejercicio de una determinada actividad profesional sea fijada por normas legales de la Unión Europea o resulte de una práctica estable y consolidada en la Unión Europea. En este ciclo de estudios se concede el grado de licenciado a los que hayan realizado los 180 créditos correspondientes a los primeros seis semestres curriculares de trabajo.

	En el segundo ciclo de estudios de las instituciones universitarias o politécnicas el título de máster se concede a los que después de aprobar todas las unidades curriculares que integran el plan de estudios del curso de máster y de aprobar el acto público de defensa de la disertación, del trabajo de proyecto o del informe de prácticas, obtengan el número de créditos fijado.


	Grado en doctor

	El grado en doctor lo conceden las universidades y los institutos universitarios a los alumnos que hayan aprobado las unidades curriculares del curso de doctorado y el acto público de defensa de la tesis.</p>
